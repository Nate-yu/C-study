#include <stdio.h>
/*
  爱因斯坦走台阶:
  有一台阶，如果每次走两阶，最后剩一阶;
  如果每次走三阶，最后剩两阶;如果每次走四阶，最后剩三阶;
  如果每次走五阶，最后剩四阶;如果每次走六阶，最后剩五阶;
  如果每次走七阶，刚好走完。求此第三少的台阶数是多少?
 */
int main() {
	int count = 1;
	int i = 1;
	while(1) {
		if(i % 2 == 1 && i % 3 == 2 && i % 4 == 3 && i % 5 == 4 && i % 6 == 5 && i % 7 == 0) {
			count++;
			if(count == 3) break;
		}
		i++;
	} 
	printf("%d",i);
	return 0;
}